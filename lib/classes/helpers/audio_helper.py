    def play_audio(file_path):
        pygame.init()
        pygame.mixer.init()

        try:
            pygame.mixer.music.load(file_path)
            pygame.mixer.music.play()

            while pygame.mixer.music.get_busy():
                pygame.time.Clock().tick(5)

        except Exception as e:
            print(f"Error: {e}")

        pygame.mixer.quit()
        pygame.quit()
